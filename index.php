                <h2><strong>ABOUT SIGIRIYA</strong></h2>
                <p id="sigiri_p_1">
                    Sigiriya, also known as the Lion Rock, is a UNESCO World Heritage Site located in the central Matale District of Sri Lanka. It is one of the country's most iconic landmarks and a popular tourist destination.

                    Sigiriya is famous for its ancient rock fortress, which dates back to the 5th century AD. The site holds historical, cultural, and archaeological significance. The fortress was built by King Kashyapa I as a royal palace complex atop a massive 200-meter-high rock. It is surrounded by lush greenery and offers stunning panoramic views of the surrounding landscape.

                    One of the notable features of Sigiriya is the entrance, known as the Lion Gate. Originally, there was a massive lion-shaped structure with a grand staircase leading to the summit. Unfortunately, only the paws of the lion remain today. The climb to the top of the rock involves ascending through a series of staircases, pathways, and ancient frescoes.
                </p>

                <p id="sigiri_p_2">
                    The highlight of Sigiriya is the Sigiriya Frescoes, a collection of well-preserved ancient paintings found on the rock face. These colorful frescoes depict celestial maidens and showcase the artistic skills of the ancient inhabitants of Sigiriya.

                    As visitors reach the summit, they encounter the remnants of the royal palace complex, including the King's throne, swimming pool, gardens, and other architectural marvels. The site also features an ancient irrigation system that demonstrates the advanced engineering skills of its builders.

                    Sigiriya holds immense historical and cultural value. It provides insights into the ancient civilization of Sri Lanka and its architectural achievements. The combination of natural beauty, historical significance, and breathtaking views makes Sigiriya a must-visit destination for travelers exploring Sri Lanka.

                </p>
                <p id="sigiri_p_3" style="display:none">
                    Dating back to the 5th century AD, Sigiriya holds great historical significance. It was transformed into a fortified palace and capital city by King Kasyapa, who sought a secure and elevated location for his kingdom. Rising 200 meters above the surrounding landscape, the rock served as a strategic vantage point and offered protection from potential invaders.

                    The architecture and design of Sigiriya are awe-inspiring. As you approach the site, you will encounter the remains of extensive gardens, reservoirs, and other structures at the base of the rock. The ascent to the top involves a series of staircases, walkways, and terraces, each unveiling intricate and fascinating details.
                </p>
